    private void rbTipeAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbTipeAActionPerformed
        // TODO add your handling code here:
        if(cbArea.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Pilih area terlebih dahulu");
            radioButton.clearSelection();
        }else{
            txtLargeLand.setText("300");
            txtLargeBuilding.setText("150");
            long landPrice = Long.parseLong(txtPriceLand.getText()),
                    landLarge = Long.parseLong(txtLargeLand.getText()),
                    buildingPrice = Long.parseLong(txtPriceBuilding.getText()),
                    buildingLarge = Long.parseLong(txtLargeBuilding.getText()),
                    Price, ppn, totalPrice;
            Price = (landPrice * landLarge) + (buildingPrice * buildingLarge);
            ppn = Price * 10 / 100;
            totalPrice = Price + ppn;
            txtPrice.setText(""+Price);
            txtPPN.setText(""+ppn);
            txtTotalPrice.setText(""+totalPrice);
            txtInstalmentAmount.setText("");
            txtDP.setText("");
            txtInstalmentMonth.setText("");
        }
    }//GEN-LAST:event_rbTipeAActionPerformed

    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        if(txtDP.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "DP harus diisi");
        }else if(txtInstalmentAmount.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Isi tenor cicilan");
        }else{
            long totalPrice = Long.parseLong(txtTotalPrice.getText()),
                dp = Long.parseLong(txtDP.getText()),
                instalmentAmount = Long.parseLong(txtInstalmentAmount.getText()),
                ppn, instalmentperMonth;
            instalmentperMonth = (totalPrice - dp)/instalmentAmount;
            txtInstalmentMonth.setText(""+instalmentperMonth);
        }
    }//GEN-LAST:event_btnProcessActionPerformed

