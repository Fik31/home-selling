    private void cbxAgreementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxAgreementActionPerformed
        // TODO add your handling code here:
        if(txtOrderName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama pemesan harus diisi");
            cbxAgreement.setSelected(false);
        }else if(txtInstalmentMonth.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tekan tombol proses terlebih dahulu");
            cbxAgreement.setSelected(false);
        }else if(cbxAgreement.isSelected()==true){
            JOptionPane.showMessageDialog(null, "Pemesan menyetujui");
            cbxAgreement.setSelected(true);
        }else{
            JOptionPane.showMessageDialog(null, "Pesanan belum disetujui");
            cbxAgreement.setSelected(false);
        }
    }//GEN-LAST:event_cbxAgreementActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(cbxAgreement.isSelected() == false){
            JOptionPane.showMessageDialog(null, "Klik setuju untuk simpan data",
                    "Error",JOptionPane.ERROR_MESSAGE);
        }else{
            String typeHouse;
            if(rbTipeA.isSelected()){
                typeHouse = "Tipe A";
            }else if(rbTipeB.isSelected()){
                typeHouse = "Tipe B";
            }else{
                typeHouse = "Tipe C";
            }
            String largeHouse;
            if(rbTipeA.isSelected()){
                largeHouse = "300m²/150m²";
            }else if(rbTipeB.isSelected()){
                largeHouse = "200m²/100m²";
            }else{
                largeHouse = "100m²/50m²";
            }
            try{
                String sql = "INSERT INTO rumah (nama, area, tipe, luas, harga, jml_cicilan, cicilan_bln) VALUES ('"
                        + txtOrderName.getText() + "','"
                        + cbArea.getSelectedItem() + "','"
                        + typeHouse + "','"
                        + largeHouse + "','"
                        + txtTotalPrice.getText() + "','"
                        + txtInstalmentAmount.getText() + "','"
                        + txtInstalmentMonth.getText() + "')";
                Connection conn = (Connection)ConnectionDb.connectDatabase();
                PreparedStatement pst = (PreparedStatement) conn.prepareStatement(sql);
                pst.execute();
            } catch (SQLException ex) {
                Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            OptionMenu optionMenu = new OptionMenu();
            optionMenu.setVisible(true);
            this.getDesktopPane().add(optionMenu);
            this.dispose();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

